package Material_Estudo;

public class Material_estudo {
	private int id;
	private String nome_Material_estudo;

	public Material_estudo() {
		super();
	}

	public Material_estudo(int id, String nome_Material_estudo) {
		super();
		this.id = id;
		this.nome_Material_estudo = nome_Material_estudo;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNome_Material_estudo() {
		return nome_Material_estudo;
	}

	public void setNome_Material_estudo(String nome_Material_estudo) {
		this.nome_Material_estudo = nome_Material_estudo;
	}

}
